        <h2>¿Sabes qué es <b>crédito por libranza</b> ?</h2>
        <p> El <span>crédito por Libranza</span> es un crédito de consumo que incluye un mecanismo de recaudo, en donde
            el deudor autoriza a su empleador o entidad pagadora para que le realice un descuento de su salario o
            pensión, con el objetivo de que esos dineros descontados, sean destinados al pago de las cuotas
            del crédito adquirido. </p>
